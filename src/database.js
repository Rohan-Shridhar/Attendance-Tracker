function getPast60WorkingDays() {
  const dates = [];
  let day = new Date();
  
  day.setDate(day.getDate() - 1);

  while (dates.length < 60) {
    const dayOfWeek = day.getDay();
    
    if (dayOfWeek > 0 && dayOfWeek < 6) {
      const isoDate = day.toISOString().split('T')[0];
      dates.push(isoDate);
    }
    
    day.setDate(day.getDate() - 1);
  }
  return dates;
}


function generateStudentStatuses() {
  const students = {};
  for (const usn in STUDENTS_DB) {
    const status = Math.random() > 0.20 ? 'Present' : 'Absent';
    
    students[usn] = { status };
  }
  return students;
}

function generateFullAttendanceDB() {
  const db = {};
  const dates = getPast60WorkingDays();
  const subjectNames = SUBJECTS.map(s => s.name); 

  for (const date of dates) {
    db[date] = {};
    
    for (const subjectName of subjectNames) {
      db[date][subjectName] = generateStudentStatuses();
    }
  }
  return db;
}

